  def _autosize_ctk_button(self, btn: ctk.CTkButton, *, extra_px: int = 34, min_px: int = 140, max_px: int = 900) -> None:
    """
    Auto-size a CTkButton to fit its current text.
    CustomTkinter doesn't auto-fit width; we measure text and set width in pixels.
    """
    try:
      text = btn.cget("text") or ""
      font = btn.cget("font")
      f = tkfont.Font(font=font)
      w = f.measure(text) + extra_px
      w = max(min_px, min(max_px, w))
      btn.configure(width=w)
    except Exception:
      # If anything fails, leave default width alone.
      pass

