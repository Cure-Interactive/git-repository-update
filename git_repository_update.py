  def _install_treeview_cell_tooltip(self, tree: "ttk.Treeview") -> None:
    """
    Treeview cell tooltip: hover shows the currently-hovered cell value.
    Mirrors the behavior used in gitea_repository_backup.py.
    """
    tip_state = {
      "win": None,
      "lbl": None,
      "font": None,  # cached tk Font for tooltip
      "last": None,  # (iid, col_id, value)
    }

    def _tip_hide() -> None:
      win = tip_state.get("win")
      if win is not None:
        try:
          win.destroy()
        except Exception:
          pass
      tip_state["win"] = None
      tip_state["lbl"] = None
      tip_state["last"] = None

    def _tip_show(*, text: str, x_root: int, y_root: int) -> None:
      # Create if needed
      if tip_state["win"] is None:
        win = tk.Toplevel(self)
        win.withdraw()
        win.overrideredirect(True)
        try:
          win.attributes("-topmost", True)
        except Exception:
          pass

        bg = _ctk_color(ctk.ThemeManager.theme["CTkFrame"]["top_fg_color"])
        fg = _ctk_color(ctk.ThemeManager.theme["CTkLabel"]["text_color"])

        # Build a larger tooltip font based on the current Tk default font
        from tkinter import font as tkfont
        if tip_state["font"] is None:
          f = tkfont.nametofont("TkDefaultFont").copy()
          try:
            f.configure(size=int(f.cget("size")) + 4)
          except Exception:
            f.configure(size=14)
          tip_state["font"] = f

        lbl = tk.Label(
          win,
          text="",
          justify="left",
          padx=8,
          pady=4,
          bg=bg,
          fg=fg,
          font=tip_state["font"],
          bd=1,
          relief="solid",
        )
        lbl.pack()

        tip_state["win"] = win
        tip_state["lbl"] = lbl

      win = tip_state["win"]
      lbl = tip_state["lbl"]
      if win is None or lbl is None:
        return

      lbl.configure(text=text)

      # Offset so it doesn't sit under the mouse
      x = x_root + 14
      y = y_root + 16

      try:
        win.geometry(f"+{x}+{y}")
        win.deiconify()
      except Exception:
        pass

    def _on_hover(event) -> None:
      iid = tree.identify_row(event.y)
      col = tree.identify_column(event.x)  # "#1", "#2", ...
      if not iid or not col or col == "#0":
        _tip_hide()
        return

      cols = list(tree["columns"])
      try:
        idx = int(col[1:]) - 1
      except Exception:
        _tip_hide()
        return

      if idx < 0 or idx >= len(cols):
        _tip_hide()
        return

      col_id = cols[idx]

      try:
        val = tree.set(iid, col_id)
      except Exception:
        _tip_hide()
        return

      text_val = str(val)

      # For time columns, show raw ISO (if available) rather than the shortened display value.
      if col_id in {"seen", "pulled"}:
        try:
          row_vals = tree.item(iid, "values") or ()
          repo_path = row_vals[1] if (row_vals and len(row_vals) >= 2) else ""
          st = self._repo_state_by_path.get(repo_path) if repo_path else None
          if st:
            raw = ""
            if col_id == "seen":
              raw = str(st.get("seen_at", "") or "")
            elif col_id == "pulled":
              raw = str(st.get("pulled_at", "") or "")
            if raw:
              text_val = raw
        except Exception:
          pass

      key = (iid, col_id, text_val)

      if tip_state["last"] != key:
        tip_state["last"] = key

      _tip_show(text=text_val, x_root=event.x_root, y_root=event.y_root)

    tree.bind("<Motion>", _on_hover)
    tree.bind("<Leave>", lambda _e: _tip_hide())
    tree.bind("<ButtonPress>", lambda _e: _tip_hide())
    tree.bind("<MouseWheel>", lambda _e: _tip_hide())     # Windows
    tree.bind("<Button-4>", lambda _e: _tip_hide())       # Linux
    tree.bind("<Button-5>", lambda _e: _tip_hide())       # Linux

  def _install_treeview_context_menu(self, tree: "ttk.Treeview") -> None:
    """
    Right-click context menu for per-repo actions:
      - Pull Selected
      - Open Selected
      - Copy Repo Path(s)
      - Copy SSH Key(s)
    """
    menu = tk.Menu(self, tearoff=0)

    def _pull_selected() -> None:
      self._on_pull_selected_clicked()

    def _open_selected() -> None:
      self._on_open_selected_clicked()

    def _copy_paths() -> None:
      paths = self._get_selected_repo_paths()
      if not paths:
        return
      self._copy_to_clipboard("\n".join(paths))

    def _copy_keys() -> None:
      keys = self._get_selected_repo_keys()
      if not keys:
        return
      self._copy_to_clipboard("\n".join(keys))

    def _select_all() -> None:
      try:
        tree.selection_set(tree.get_children())
      except Exception:
        pass

    menu.add_command(label="⬇️ Pull Selected", command=_pull_selected)
    menu.add_separator()
    menu.add_command(label="📂 Open Selected", command=_open_selected)
    menu.add_separator()
    menu.add_command(label="📋 Copy Repo Path(s)", command=_copy_paths)
    menu.add_command(label="📋 Copy SSH Key(s)", command=_copy_keys)
    menu.add_separator()
    menu.add_command(label="Select All", command=_select_all)

