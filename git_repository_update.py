  def _install_treeview_fit_columns(self, tree: "ttk.Treeview", frame: "ctk.CTkFrame") -> None:
    """
    Debounced “fit” behavior for the two stretch columns (repo/key).

    - Keeps fixed columns fixed.
    - Reallocates leftover width between repo/key.
    - Prevents jitter by debouncing <Configure>.
    """
    def _fit_now() -> None:
      try:
        w = int(tree.winfo_width() or 0)
      except Exception:
        return

      if w <= 0:
        return

      # Fixed columns
      fixed = 0
      for col in ("idx", "seen", "pulled", "status"):
        try:
          fixed += int(tree.column(col, "width") or 0)
        except Exception:
          pass

      # Scrollbar gutter / padding fudge (ttk varies a bit)
      gutter = 26
      avail = max(0, w - fixed - gutter)

      repo_min = 180
      key_min = 140

      if avail <= (repo_min + key_min):
        repo_w = repo_min
        key_w = key_min
      else:
        # Weight repo a little heavier than key
        repo_w = int(avail * 0.60)
        key_w = avail - repo_w
        repo_w = max(repo_w, repo_min)
        key_w = max(key_w, key_min)

      try:
        tree.column("repo", width=repo_w)
        tree.column("key", width=key_w)
      except Exception:
        pass

    def _debounced(_e=None) -> None:
      try:
        if getattr(self, "_fit_after_id", None):
          self.after_cancel(self._fit_after_id)
      except Exception:
        pass

      self._fit_after_id = self.after(120, _fit_now)

    frame.bind("<Configure>", _debounced)
    tree.bind("<Map>", _debounced)

    # Initial fit
    _debounced()

