def _abs_from_script(path: str) -> str:
  if not path:
    return path
  if os.path.isabs(path):
    return os.path.abspath(path)
  return os.path.abspath(os.path.join(_SCRIPT_DIR, path))


def path_safe(path: str) -> str:
  """
  Normalize + absolute + forward slashes.
  IMPORTANT: relative paths are resolved relative to THIS script's folder.
  """
  if not path:
    return path
  return os.path.normpath(_abs_from_script(path)).replace("\\", "/")


def read_json(path: str, *, required: bool = True, default: Any = None) -> Any:
  if not os.path.exists(path):
    if required:
      raise FileNotFoundError(path)
    return default
  with open(path, "r", encoding="utf-8") as f:
    return json.load(f)


def write_json(path: str, data: Any, *, indent: int = 2) -> None:
  parent = os.path.dirname(path)
  if parent:
    os.makedirs(parent, exist_ok=True)
  with open(path, "w", encoding="utf-8") as f:
    json.dump(data, f, indent=indent)


def _copy_if_missing(dst: str, src: str, *, overwrite: bool = False) -> bool:
  os.makedirs(os.path.dirname(dst), exist_ok=True)
  if os.path.exists(dst) and not overwrite:
    return False
  if not os.path.exists(src):
    raise FileNotFoundError(src)
  shutil.copy2(src, dst)
  return True


