  def _do_refresh(self) -> None:
    self._ui_q.put(("status", "Refreshing…"))

    preflight("refresh")
    cfg = read_json(_CONFIG_JSON, required=True)

    # Unified config: scan + filters (back-compat to refresh + pull)
    scan_cfg = cfg.get("scan", {}) if isinstance(cfg, dict) else {}
    if not scan_cfg:
      scan_cfg = cfg.get("refresh", {}) if isinstance(cfg, dict) else {}

    filters_cfg = cfg.get("filters", {}) if isinstance(cfg, dict) else {}
    if not filters_cfg:
      filters_cfg = cfg.get("pull", {}) if isinstance(cfg, dict) else {}

    scan_roots = scan_cfg.get("scan_roots", [])
    ignore_prefixes = scan_cfg.get("ignore_prefixes", ["$"])
    default_ssh_key = scan_cfg.get("default_ssh_key", "")

    scan_roots = [s for s in scan_roots if isinstance(s, str)]
    ignore_prefixes = [s for s in ignore_prefixes if isinstance(s, str)]

    allowed_prefixes = [path_safe(p) for p in filters_cfg.get("allowed_roots", []) if isinstance(p, str)]
    disallowed_prefixes = [path_safe(p) for p in filters_cfg.get("disallowed_roots", []) if isinstance(p, str)]

    def _passes_filters(repo_path: str) -> bool:
      safe_path = path_safe(repo_path)

      if any(safe_path.startswith(disallowed) for disallowed in disallowed_prefixes):
        return False

      if allowed_prefixes and not any(safe_path.startswith(allowed) for allowed in allowed_prefixes):
        return False

      return True

    manifest_json_abs = path_safe(_REPOSITORY_MANIFEST_JSON)
    default_ssh_key_abs = path_safe(default_ssh_key) if default_ssh_key else ""

    self._log("info", "------------------------------------------------------------")
    self._log("info", "BEGIN REFRESH (scan + apply filters + write manifest v2)")
    self._log("info", "------------------------------------------------------------")

    if not scan_roots:
      self._log("warn", "⚠️  scan_roots is empty. Nothing to scan.")
      return

    # Load existing manifest v2 (preserve pulled_at / last_error etc)
    manifest: ManifestV2 = load_manifest_v2(manifest_json_abs)
    byp = repos_by_path(manifest)

    # Reset UI table for fresh scan display
    self._ui_q.put(("tree_clear", None))

    skipped_by_filter = 0
    total_roots = len(scan_roots)

    for i, root in enumerate(scan_roots, start=1):
      if self._worker_stop.is_set():
        self._log("warn", "🛑 Stop requested; ending refresh early (no manifest write).")
        return

      self._ui_q.put(("status", f"Refreshing... ({i} of {total_roots} root{'' if total_roots == 1 else 's'})"))

      root_abs = path_safe(root)
      if not root_abs or not os.path.isdir(root_abs):
        self._log("warn", f"⚠️  Skipping missing scan root: {root_abs}")
        self._ui_q.put(("progress", min(1.0, i / max(1, total_roots))))
        continue

      repo_keys = find_git_repos_with_keys(root_abs, default_ssh_key_abs, ignore_prefixes, self._log)
      for repo_path, key in repo_keys.items():

        repo_path = path_safe(repo_path)
        key = path_safe(key)

        if not _passes_filters(repo_path):
          skipped_by_filter += 1
          continue

        seen = now_iso()

        # Preserve prior state if present
        r = byp.get(repo_path)
        if r is None:
          r = upsert_repo(
            manifest,
            repo_path,
            ssh_key=key,
            phase="idle",
            message="Found",
            seen_at=seen,
          )
          byp[repo_path] = r
        else:
          upsert_repo(
            manifest,
            repo_path,
            ssh_key=key,
            phase=str(r.get("phase", "idle") or "idle"),
            message="Found",
            seen_at=seen,
          )

        # Update in-memory map used by tooltips
        self._repo_state_by_path[repo_path] = byp[repo_path]

        self._ui_q.put(("tree_add", (repo_path, key, "Found", ts_short(seen), ts_short(str(r.get("pulled_at", "") or "")))))

      self._ui_q.put(("progress", min(1.0, i / max(1, total_roots))))

    dry = self.var_dry_run.get()
    if dry:
      self._log("warn", f"🧪 DRY RUN: Would write v2 manifest with {len(manifest.get('repos', []) or [])} repos to: {manifest_json_abs}")
    else:
      save_manifest_v2(manifest_json_abs, manifest, dry_run=False)
      self._log("ok", f"✅ Saved v2 manifest ({len(manifest.get('repos', []) or [])} repos) to {manifest_json_abs}")

    if skipped_by_filter:
      self._log("info", f"ℹ️  Skipped by filters: {skipped_by_filter}")

    self._log("info", "------------------------------------------------------------")
    self._log("ok", "END REFRESH")
    self._log("info", "------------------------------------------------------------")

  def _do_pull(self) -> None:
    self._ui_q.put(("status", "Pulling…"))

    preflight("pull")

    repo_list_path = _REPOSITORY_MANIFEST_JSON
    pull_filters_path = _CONFIG_JSON

    self._log("info", "------------------------------------------------------------")
    self._log("info", "BEGIN PULL (git pull --ff-only)")
    self._log("info", "------------------------------------------------------------")

    try:
      subprocess.run(["git", "--version"], check=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE, text=True)
    except Exception as e:
      self._log("fail", f"❌ git not found or not runnable: {e}")
      return

    manifest, repos = load_repo_manifest_for_pull(repo_list_path, pull_filters_path)
    if not repos:
      self._log("warn", "⚠️  No repositories matched your filters.")
      return

    if self._pull_only_paths:
      only = self._pull_only_paths
      repos = [r for r in repos if path_safe(str(r.get("path", "") or "")) in only]

      if not repos:
        self._log("warn", "⚠️  None of the selected repos matched your current filters.")
        return

      self._log("info", f"🎯 Pull Selected enabled: {len(repos)} repo{'' if len(repos) == 1 else 's'}")

    # Update in-memory state map
    self._repo_state_by_path = repos_by_path(manifest)

    # Keep the full table visible.
    # Only mark TARGET repos as queued (do NOT clear/rebuild the table).
    for r in repos:
      repo_path = path_safe(str(r.get("path", "") or ""))
      ssh_key = path_safe(str(r.get("ssh_key", "") or ""))
      seen = ts_short(str(r.get("seen_at", "") or ""))
      pulled = ts_short(str(r.get("pulled_at", "") or ""))
      self._ui_q.put(("tree_update_by_repo", (repo_path, ssh_key, "Queued", seen, pulled)))

    dry = self.var_dry_run.get()
    if dry:
      self._log("warn", "🧪 DRY RUN MODE: No git commands will be executed.")

    changed_repos: List[str] = []
    warn_repos: List[Tuple[str, str]] = []
    error_repos: List[Tuple[str, str]] = []

    total = len(repos)
    for idx, r in enumerate(repos, start=1):
      if self._worker_stop.is_set():
        self._log("warn", f"🛑 Stop requested; stopping after {idx - 1} of {total} repo{'' if total == 1 else 's'}.")
        break

      repo_path = path_safe(str(r.get("path", "") or ""))
      ssh_key = path_safe(str(r.get("ssh_key", "") or ""))

      self._ui_q.put(("status", f"Pulling... ({idx} of {total} repo{'' if total == 1 else 's'})"))

      # Mark pulling + update last-seen
      seen_iso = now_iso()
      upsert_repo(manifest, repo_path, phase="pulling", message="Pulling...", seen_at=seen_iso, last_error="")
      self._repo_state_by_path[repo_path] = repos_by_path(manifest).get(repo_path, RepoState(path=repo_path))  # refresh mapping
      self._ui_q.put(("tree_update_by_repo", (repo_path, ssh_key, "Pulling...", ts_short(seen_iso), ts_short(str(r.get("pulled_at", "") or "")))))

      if dry:
        self._log("info", f"🟡 [Dry Run] Would pull: {repo_path} (SSH key: {ssh_key})")
        upsert_repo(manifest, repo_path, phase="idle", message="Dry run", seen_at=seen_iso)
        self._repo_state_by_path[repo_path] = repos_by_path(manifest).get(repo_path, RepoState(path=repo_path))
        self._ui_q.put(("tree_update_by_repo", (repo_path, ssh_key, "Dry run", ts_short(seen_iso), ts_short(str(r.get("pulled_at", "") or "")))))
        continue

      success, error_msg, changed = git_pull_ff_only(repo_path, ssh_key)

      if not success and error_msg:
        em = (error_msg or "").lower()

        # Non-error "skipped" states (YELLOW)
        if "unborn" in em or "no commits" in em:
          msg = "Unborn HEAD (no commits)"
          self._log("warn", f"🟨 {repo_path} - {msg}.")
          warn_repos.append((repo_path, msg))

          upsert_repo(manifest, repo_path, phase="skipped", message="Unborn (no commits)", seen_at=seen_iso, last_error="")
          self._repo_state_by_path[repo_path] = repos_by_path(manifest).get(repo_path, RepoState(path=repo_path))
          self._ui_q.put(("tree_update_by_repo", (repo_path, ssh_key, "Unborn (no commits)", ts_short(seen_iso), ts_short(str(r.get("pulled_at", "") or "")))))

        elif "bare repository" in em or "no work tree" in em or "work tree" in em:
          msg = "Bare repository (no work tree)"
          self._log("warn", f"🟨 {repo_path} - {msg}.")
          warn_repos.append((repo_path, msg))

          upsert_repo(manifest, repo_path, phase="skipped", message="Bare repo (no work tree)", seen_at=seen_iso, last_error="")
          self._repo_state_by_path[repo_path] = repos_by_path(manifest).get(repo_path, RepoState(path=repo_path))
          self._ui_q.put(("tree_update_by_repo", (repo_path, ssh_key, "Bare repo (no work tree)", ts_short(seen_iso), ts_short(str(r.get("pulled_at", "") or "")))))

        else:
          # Real error (RED)
          self._log("fail", f"❌ {repo_path} - {error_msg}")
          error_repos.append((repo_path, error_msg))

          upsert_repo(manifest, repo_path, phase="error", message="Error", seen_at=seen_iso, last_error=error_msg)
          self._repo_state_by_path[repo_path] = repos_by_path(manifest).get(repo_path, RepoState(path=repo_path))
          self._ui_q.put(("tree_update_by_repo", (repo_path, ssh_key, "Error", ts_short(seen_iso), ts_short(str(r.get("pulled_at", "") or "")))))

      else:
        pulled_iso = now_iso()

        if changed:
          self._log("ok", f"✅ {repo_path} - Updated.")
          changed_repos.append(repo_path)
          upsert_repo(manifest, repo_path, phase="done", message="Updated", seen_at=seen_iso, pulled_at=pulled_iso, last_error="")
          self._ui_q.put(("tree_update_by_repo", (repo_path, ssh_key, "Updated", ts_short(seen_iso), ts_short(pulled_iso))))
        else:
          self._log("ok", f"✅ {repo_path} - Done (no changes).")
          upsert_repo(manifest, repo_path, phase="done", message="Done (no changes)", seen_at=seen_iso, pulled_at=pulled_iso, last_error="")
          self._ui_q.put(("tree_update_by_repo", (repo_path, ssh_key, "Done (no changes)", ts_short(seen_iso), ts_short(pulled_iso))))

        self._repo_state_by_path[repo_path] = repos_by_path(manifest).get(repo_path, RepoState(path=repo_path))

      # Persist v2 manifest as we go (crash-safe progress)
      if not dry:
        save_manifest_v2(path_safe(repo_list_path), manifest, dry_run=False)

      self._ui_q.put(("progress", min(1.0, idx / max(1, total))))

    self._log("info", "------------------------------------------------------------")
    self._log("info", "RESULT")
    self._log("info", "------------------------------------------------------------")

    self._log("info", "Repositories with changes pulled:")
    if changed_repos:
      for rr in changed_repos:
        self._log("ok", f"  ✅ {rr}")
    else:
      self._log("warn", "  (none)")

    self._log("info", "")
    self._log("info", "Repositories skipped (non-error):")
    if warn_repos:
      for rr, msg in warn_repos:
        self._log("warn", f"  🟨 {rr} - {msg}")
    else:
      self._log("warn", "  (none)")

    self._log("info", "")
    self._log("info", "Repositories with errors:")
    if error_repos:
      for rr, msg in error_repos:
        self._log("fail", f"  ❌ {rr} - {msg}")
    else:
      self._log("warn", "  (none)")

    self._log("info", "------------------------------------------------------------")
    self._log("ok", "END PULL")
    self._log("info", "------------------------------------------------------------")


