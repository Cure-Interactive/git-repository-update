def is_repo_unborn(repo_path: str) -> bool:
  try:
    subprocess.run(
      ["git", "-C", repo_path, "rev-parse", "--verify", "HEAD"],
      check=True,
      stdout=subprocess.PIPE,
      stderr=subprocess.PIPE,
      text=True,
    )
    return False
  except subprocess.CalledProcessError:
    return True


def is_repo_bare(repo_path: str) -> bool:
  """
  True if the target path is a *bare* git repository (no working tree).

  This is treated as a non-error "skipped" state for this tool, since
  `git pull` requires a work tree.
  """
  try:
    r = subprocess.run(
      ["git", "-C", repo_path, "rev-parse", "--is-bare-repository"],
      check=True,
      stdout=subprocess.PIPE,
      stderr=subprocess.PIPE,
      text=True,
    )
    return (r.stdout or "").strip().lower() == "true"
  except Exception:
    return False


def git_pull_ff_only(repo_path: str, ssh_key: str) -> Tuple[bool, Optional[str], bool]:
  """
  Returns: (success, error_msg, changed)

  Notes:
  - Bare repos are *skipped* (non-error) because `git pull` requires a work tree.
  - Unborn HEAD repos are *skipped* (non-error) because there are no commits/branches yet.
  """
  env = os.environ.copy()
  if ssh_key:
    env["GIT_SSH_COMMAND"] = f'plink -i "{ssh_key}"'

  if is_repo_bare(repo_path):
    return False, "Bare repository (no work tree). Skipping.", False

  if is_repo_unborn(repo_path):
    return False, "Unborn HEAD (no commits). Skipping.", False

  try:
    result = subprocess.run(
      ["git", "-C", repo_path, "pull", "--ff-only"],
      check=True,
      stdout=subprocess.PIPE,
      stderr=subprocess.PIPE,
      text=True,
      env=env,
    )
    out = (result.stdout or "").strip()
    changed = ("Already up to date." not in out) and ("Already up-to-date." not in out)
    return True, None, changed
  except subprocess.CalledProcessError as e:
    stderr = (e.stderr or "").strip()
    stdout = (e.stdout or "").strip()
    msg = stderr or stdout or str(e)
    return False, msg, False


def load_repo_manifest_for_pull(repo_json: str, pull_filters_json: str) -> Tuple[ManifestV2, List[RepoState]]:
  """
  Loads repository_manifest.json (any supported format) and returns:
    (manifest_v2, filtered_repos)

  Filtering is driven by config.json (filters.allowed_roots / filters.disallowed_roots)
  with back-compat to pull.allowed_roots / pull.disallowed_roots.
  """
  manifest: ManifestV2 = load_manifest_v2(repo_json)

  cfg = read_json(pull_filters_json, required=True)
  filters_cfg = cfg.get("filters", {}) if isinstance(cfg, dict) else {}
  if not filters_cfg:
    filters_cfg = cfg.get("pull", {}) if isinstance(cfg, dict) else {}

  allowed_prefixes = [path_safe(p) for p in filters_cfg.get("allowed_roots", []) if isinstance(p, str)]
  disallowed_prefixes = [path_safe(p) for p in filters_cfg.get("disallowed_roots", []) if isinstance(p, str)]

  out: List[RepoState] = []
  for r in (manifest.get("repos", []) or []):
    repo_path = path_safe(str(r.get("path", "") or ""))
    if not repo_path:
      continue

    if any(repo_path.startswith(disallowed) for disallowed in disallowed_prefixes):
      continue

    if allowed_prefixes and not any(repo_path.startswith(allowed) for allowed in allowed_prefixes):
      continue

    # Normalize fields
    r["path"] = repo_path
    r["ssh_key"] = path_safe(str(r.get("ssh_key", "") or ""))
    if not r.get("message"):
      r["message"] = "Idle"
    if not r.get("phase"):
      r["phase"] = "idle"

    out.append(r)

  return manifest, out


