"""
Repository Bulk

What this does (single-file GUI replacement for your 2 scripts):
- Refresh tab:
  - Scan configured root folders for git repositories (folders containing ".git").
  - Extract an SSH key per repo from:
      1) repo .git/config (IdentityFile / PuTTYKeyFile / sshCommand -i ...)
      2) TortoiseGit per-project registry settings (Windows only)
      3) TortoiseGit global registry settings (Windows only)
      4) fallback default_ssh_key from refresh config
  - Writes the mapping { repo_path: ssh_key_path } to repository_manifest.json (hard-coded next to the script)

- Pull tab:
  - Loads the repo list JSON generated by refresh.
  - Applies optional allowed_roots / disallowed_roots filtering (pull config).
  - Runs `git pull --ff-only` for each repo (skips unborn repos with no commits).
  - Uses per-repo SSH key by setting:
      GIT_SSH_COMMAND = plink -i "<key>"
    (matches your existing pull script behavior; Windows-oriented)

Folder layout assumption (matches your tree):
<repo_bulk_dir>/
  repository_bulk_gui.py          <-- this file
  repository_bulk_refresh.py
  repository_bulk_pull.py
  config/
    repository_bulk_refresh.json
    repository_bulk_pull.json
    repository_bulk_list.json
    default/
      repository_bulk_refresh.json
      repository_bulk_pull.json

Template compatibility:
- If your default template filenames differ, this script also checks legacy names:
    config/default/repo_list_refresh.json
    config/default/repo_list_pull.json

Dependencies:
- customtkinter

Install:
  pip install customtkinter

Run:
  python repository_bulk_gui.py
"""

