  def _seed_repo_table_on_start(self) -> None:
    """
    Populate the repo table at startup from repository_manifest.json (if present).

    Supports:
    - v2 manifest: { "version": 2, "repos": [...] }
    - v1 mapping: { "<repo_path>": "<ssh_key>", ... }
    - legacy list: [ [ "<repo_path>", "<ssh_key>" ], ... ]
    """
    try:
      manifest_path = path_safe(_REPOSITORY_MANIFEST_JSON)

      manifest: ManifestV2 = load_manifest_v2(manifest_path)
      repos = manifest.get("repos", []) or []

      self._repo_state_by_path = repos_by_path(manifest)

      # Clear + repopulate
      for iid in self.tree.get_children():
        self.tree.delete(iid)

      for r in repos:
        repo_path = path_safe(str(r.get("path", "") or ""))
        ssh_key = path_safe(str(r.get("ssh_key", "") or ""))
        status = str(r.get("message", "") or "Idle") or "Idle"
        seen = ts_short(str(r.get("seen_at", "") or ""))
        pulled = ts_short(str(r.get("pulled_at", "") or ""))

        if repo_path:
          self._tree_add_row(repo_path, ssh_key, status, idx=int(r.get("index", 0) or 0) or None, last_seen=seen, last_pull=pulled)

      try:
        self.lbl_status.configure(text=f"Idle ({len(repos)} repos loaded)")
      except Exception:
        pass

      self._log("ok", f"📋 Loaded {len(repos)} repos into table from: {manifest_path}")

    except Exception as e:
      self._log("warn", f"⚠️  Could not seed repo table from manifest: {e}")

  def _populate_repo_table_on_start(self) -> None:
    """
    Legacy hook kept for safety. Uses the new manifest-based seeding.
    """
    self._seed_repo_table_on_start()

  def _load_refresh_settings_safe(self) -> RefreshSettings:
    try:
      ensure_defaults(overwrite=False)
      cfg = read_json(_CONFIG_JSON, required=True)

      # New unified naming: "scan"
      # Back-compat: accept legacy "refresh".
      r = cfg.get("scan", {}) if isinstance(cfg, dict) else {}
      if not r:
        r = cfg.get("refresh", {}) if isinstance(cfg, dict) else {}
    except Exception:
      r = {}

    scan_roots = [s for s in r.get("scan_roots", []) if isinstance(s, str)]
    ignore_prefixes = [s for s in r.get("ignore_prefixes", []) if isinstance(s, str)]
    default_ssh_key = r.get("default_ssh_key", "") if isinstance(r.get("default_ssh_key", ""), str) else ""

    return RefreshSettings(
      scan_roots=scan_roots or [""],
      ignore_prefixes=ignore_prefixes or ["$", "System Volume Information"],
      default_ssh_key=default_ssh_key,
    )

  def _load_pull_settings_safe(self) -> PullSettings:
    try:
      ensure_defaults(overwrite=False)
      cfg = read_json(_CONFIG_JSON, required=True)

      # New unified naming: "filters"
      # Back-compat: accept legacy "pull".
      p = cfg.get("filters", {}) if isinstance(cfg, dict) else {}
      if not p:
        p = cfg.get("pull", {}) if isinstance(cfg, dict) else {}
    except Exception:
      p = {}

    allowed_roots = [s for s in p.get("allowed_roots", []) if isinstance(s, str)]
    disallowed_roots = [s for s in p.get("disallowed_roots", []) if isinstance(s, str)]
    return PullSettings(allowed_roots=allowed_roots, disallowed_roots=disallowed_roots)

