  def _log(self, level: str, msg: str) -> None:
    self._log_q.put((level, msg))

  def _clear_log(self) -> None:
    self.txt_log.delete("1.0", "end")

  def _pump_queues(self) -> None:
    # Pump log lines
    while True:
      try:
        level, msg = self._log_q.get_nowait()
      except queue.Empty:
        break
      ts = time.strftime("%H:%M:%S")
      line = f"[{ts}] {msg}\n"
      self.txt_log.insert("end", line)
      self.txt_log.see("end")

    # Pump UI actions
    while True:
      try:
        kind, payload = self._ui_q.get_nowait()
      except queue.Empty:
        break

      if kind == "status":
        self.lbl_status.configure(text=str(payload))
      elif kind == "progress":
        try:
          self.progress.set(float(payload))
        except Exception:
          pass
      elif kind == "tree_clear":
        for iid in self.tree.get_children():
          self.tree.delete(iid)
      elif kind == "tree_add":
        # payload: (repo, key, status, last_seen, last_pull)
        repo, key, status, last_seen, last_pull = payload
        self._tree_add_row(repo, key, status, last_seen=last_seen, last_pull=last_pull)

      elif kind == "tree_update_by_repo":
        # payload: (repo, key, status, last_seen, last_pull)
        repo, key, status, last_seen, last_pull = payload
        self._tree_update(repo, key, status, last_seen=last_seen, last_pull=last_pull)

    self.after(80, self._pump_queues)

  def _tree_add_row(
    self,
    repo: str,
    key: str,
    status: str,
    *,
    idx: Optional[int] = None,
    last_seen: str = "—",
    last_pull: str = "—",
  ) -> None:
    next_idx = idx if (idx is not None) else (len(self.tree.get_children()) + 1)
    zebra = "even" if ((next_idx) % 2 == 0) else "odd"
    tags = (self._tree_compose_row_tag(status, zebra),)
    self.tree.insert("", "end", values=(str(next_idx), repo, key, last_seen, last_pull, status), tags=tags)

  def _tree_update(
    self,
    repo: str,
    key: str,
    status: str,
    *,
    last_seen: Optional[str] = None,
    last_pull: Optional[str] = None,
  ) -> None:
    # Find row by repo (repo is column #2 -> index 1)
    for iid in self.tree.get_children():
      vals = self.tree.item(iid, "values")
      if vals and len(vals) >= 6 and vals[1] == repo:
        zebra = self._tree_zebra_from_existing_row(iid)
        tags = (self._tree_compose_row_tag(status, zebra),)

        row_idx = vals[0]
        seen = last_seen if (last_seen is not None) else (vals[3] if len(vals) >= 4 else "—")
        pulled = last_pull if (last_pull is not None) else (vals[4] if len(vals) >= 5 else "—")

        self.tree.item(iid, values=(row_idx, repo, key, seen, pulled, status), tags=tags)
        return

    # If missing, append as a new row
    self._tree_add_row(repo, key, status, last_seen=(last_seen or "—"), last_pull=(last_pull or "—"))

  def _tree_status_tag_base(self, status: str) -> Optional[str]:
    s = (status or "").strip().lower()
    if not s:
      return None

    # Warning (non-error) - should be YELLOW
    if "bare" in s or "unborn" in s or "no commits" in s:
      return "status_warn"

    # Error - should be RED
    if "error" in s or "fail" in s:
      return "status_error"

    # Success / Done - should be GREEN
    if "updated" in s or "done" in s or "success" in s or "no changes" in s:
      return "status_done"

    # Idle / queued / found / dry-run - should be GREY
    if "idle" in s or "queued" in s or "dry run" in s or "found" in s:
      return "status_idle"

    # In-progress (BLUE) — this is specifically a git pull in this tool.
    if (
      "pull" in s
      or "pulling" in s
      or "sync" in s
      or "fetch" in s
      or "update" in s
      or "clon" in s
      or "clone" in s
    ):
      return "status_pulling"

    return None

  def _tree_zebra_from_existing_row(self, iid: str) -> str:
    tags = self.tree.item(iid, "tags") or ()
    for t in tags:
      if t.endswith("_even") or t == "even":
        return "even"
      if t.endswith("_odd") or t == "odd":
        return "odd"

    # Fallback: derive from current order
    children = list(self.tree.get_children())
    try:
      idx = children.index(iid) + 1
    except ValueError:
      idx = len(children) + 1
    return "even" if (idx % 2 == 0) else "odd"

  def _tree_compose_row_tag(self, status: str, zebra: str) -> str:
    base = self._tree_status_tag_base(status)
    return f"{base}_{zebra}" if base else zebra

