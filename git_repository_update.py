def should_skip_dir(dirname: str, ignore_prefixes: List[str]) -> bool:
  return any(dirname.startswith(prefix) for prefix in ignore_prefixes)


def _parse_identity_from_sshcommand(value: str) -> str:
  # -i "path" OR -i path
  m = re.search(r'(?:^|\s)-i\s+(?:"([^"]+)"|(\S+))', value)
  if not m:
    return ""
  return (m.group(1) or m.group(2) or "").strip()


def extract_ssh_key_from_config(repo_path: str) -> str:
  config_path = os.path.join(repo_path, ".git", "config")
  if not os.path.exists(config_path):
    return ""

  try:
    with open(config_path, "r", encoding="utf-8") as f:
      for raw in f:
        line = raw.strip()
        lower = line.lower()

        if "identityfile" in lower and "=" in line:
          return line.split("=", 1)[-1].strip().strip('"').strip("'")

        if "puttykeyfile" in lower and "=" in line:
          return line.split("=", 1)[-1].strip().strip('"').strip("'")

        if "sshcommand" in lower and "=" in line:
          value = line.split("=", 1)[-1].strip().strip('"').strip("'")
          found = _parse_identity_from_sshcommand(value)
          if found:
            return found
  except OSError:
    return ""

  return ""


def _registry_project_key_variants(repo_path: str) -> List[str]:
  r"""
  TortoiseGit stores keys under:
    HKCU\Software\TortoiseGit\Projects\<path>
  """
  p = repo_path.rstrip("\\/").replace("/", "\\")
  variants = {p, p.lower()}

  if len(p) >= 2 and p[1] == ":":
    variants.add(p[0].upper() + p[1:])
    variants.add(p[0].lower() + p[1:])
    variants.add((p[0].upper() + p[1:]).lower())
    variants.add((p[0].lower() + p[1:]).lower())

  return list(variants)


def extract_ssh_key_from_registry(repo_path: str) -> str:
  if not _HAS_WINREG:
    return ""

  for variant in _registry_project_key_variants(repo_path):
    key_path = f"Software\\TortoiseGit\\Projects\\{variant}"
    try:
      with winreg.OpenKey(winreg.HKEY_CURRENT_USER, key_path) as key:  # type: ignore
        for i in range(0, 50):
          try:
            name, value, _ = winreg.EnumValue(key, i)  # type: ignore
            if name.lower() in {"keyfile", "sshkey", "puttykey", "identityfile"}:
              return value
          except OSError:
            break
    except FileNotFoundError:
      continue
    except OSError:
      continue

  return ""


def extract_ssh_key_from_global_settings() -> str:
  if not _HAS_WINREG:
    return ""
  try:
    with winreg.OpenKey(winreg.HKEY_CURRENT_USER, r"Software\TortoiseGit") as key:  # type: ignore
      for i in range(0, 50):
        try:
          name, value, _ = winreg.EnumValue(key, i)  # type: ignore
          if name.lower() in {"puttykey", "sshkey", "keyfile", "identityfile"}:
            return value
        except OSError:
          break
  except FileNotFoundError:
    pass
  except OSError:
    pass
  return ""


def find_git_repos_with_keys(
  root_path: str,
  default_key: str,
  ignore_prefixes: List[str],
  log_fn,
) -> Dict[str, str]:
  repo_keys: Dict[str, str] = {}

  for dirpath, dirnames, _filenames in os.walk(root_path):
    dirnames[:] = [d for d in dirnames if not should_skip_dir(d, ignore_prefixes)]

    if ".git" in dirnames:
      repo_path = path_safe(dirpath)
      ssh_key = (
        extract_ssh_key_from_config(repo_path)
        or extract_ssh_key_from_registry(repo_path)
        or extract_ssh_key_from_global_settings()
        or default_key
      )
      repo_keys[repo_path] = path_safe(ssh_key) if ssh_key else ""
      dirnames[:] = []
      if ssh_key:
        log_fn("info", f"🔑 Found repo: {repo_path}  (key: {repo_keys[repo_path]})")
      else:
        log_fn("warn", f"🔑 Found repo: {repo_path}  (key: <none>)")

  return repo_keys


