  def _run_worker(self, fn) -> None:
    if self._worker and self._worker.is_alive():
      messagebox.showwarning("Busy", "An operation is already running.")
      return

    self._worker_stop.clear()
    self._set_running(True)

    def _wrap():
      try:
        fn()
      except Exception as e:
        self._log("fail", f"❌ Unhandled error: {e}")
      finally:
        self._pull_only_paths = None
        self._ui_q.put(("progress", 0.0))
        self._ui_q.put(("status", "Idle"))
        self._set_running(False)

    self._worker = threading.Thread(target=_wrap, daemon=True)
    self._worker.start()

